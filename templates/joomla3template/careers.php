   	  <!-- Web Developer Listing -->
   	  <h3><a id="career_title" NAME="webdeveloper">Web Developer</a></h3>
   	  <h4>Responsibilites:</h4>
   	  <p>- Build and maintain massively-scalable public-facing web properties</p>
   	  <p>- Build portals, web services, and internal tools</p>
   	  <h4>Requirements:</h4>
   	  <p>- Fully proficient in object-oriented PHP5 (NodeJS is a plus)</p>
   	  <p>- Fully proficient with relational databases (i.e. MySQL, Postgres)</p>
   	  <p>- Familiar with NoSQL databases (namely Riak, Cassandra/Solr)</p>
   	  <p>- Familiar with Apache or NginX</p>
   	  <p>- Experience working with the PHPUnit or SimpleTest testing framework</p>
   	  <p>- Experience working with MVC frameworks (i.e. Yii, YUI, Zend, CakePHP)</p>
   	  <p>- Experience hand-coding HTML5, CSS and Javascript</p>
   	  <p>- Experience with version control systems (i.e. Git)</p>
   	  <p>- Basic UNIX/Linux shell proficiency</p>
   	  <p>- Basic understanding of build tools (i.e. Ant, Phing)</p>
   	  <p>- You can set up your own development environment (i.e. installing Apache, configuring vhosts, configuring PHP5 and necessary dependencies)</p>
   	  <p>If you are interested in this position, please email your resume to <a id="mailto" href="mailto:hr@vibin.it">HR@Vibin.it</a> and put "web Developer" in the subject line.</p>
