   	  <!-- Senior App Developer Listing -->
   	  <h3><a id="career_title" NAME="seniorappdeveloper">Senior App Developer</a></h3>
   	  <h4>Responsibilites:</h4>
   	  <p>- Develop and delploy iOS based applications using Objective-C/Cocoa and C++; and/or Android based applications using Java and the Android SDK</p>
   	  <p>- Work collaboratively or in isolation as the situation demands</p>
   	  <p>- Design, develop and test complete applications and also discrete components of larger apps</p>
   	  <p>- Interface with remote and distributed data sources and APIs</p>
   	  <p>- Troubleshoot, optimise and performance tune</p>
   	  <h4>Requirements:</h4>
   	  <p>- At least 2 years experience writing apps for Android or iOS</p>
   	  <p>- Solid understanding of Cocoa/UIKit frameworks and the Android SDK and /or iOS SDK</p>
   	  <p>- Applications released on the Google Play (Android Market) or iTunes App Store</p>
   	  <p>- Experience with real-tim graphics and advanced rendering techniques (i.e. Open GL ES)</p>
   	  <p>- Experience with building advanced User Interfaces</p>
   	  <p>- Knowledge of server side web application development</p>
	  <p>- Knowledge of Javascript/JQuery, CSS and HTML</p>
	  <p>- General technology troubleshooting skills using Unix tools</p>
	  <p>- Proficiency in the Xcode tool chain and/or experience with building Android projects from the command line with Ant or via Eclipse</p>
	  <p>- Exposure to Agile methodologies</p>
	  <p>- Solid experience of object-oriented design and modern programming techniques</p>
	  <p>- Demonstrable algorithm/data structure and problem solving skills</p>
	  <p>- Demonstrable track record of published and successful applications</p>
	  <p>If you are interested in this position, please email your resume to <a id="mailto" href="mailto:hr@vibin.it">HR@Vibin.it</a> and put "Senior App Developer" in the subject line.</p>
